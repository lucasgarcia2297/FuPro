#include <iostream> 
using namespace std;
int main(int argc, char *argv[]) { 
	// una variable numérica entera 
	int a=1;
	cout<<a<<endl; 
	
	// una variable de tipo char 
	char b='@'; 
	cout<<b<<endl; 
	
	// una variable de tipo float 
	float c = 3.14; 
	cout<<c<<endl; 
	
	// una variable de tipo double 
	double d= 3.254547E+8; 
	cout<<d<<endl;
	
	// una variable de tipo string 
	string e="Hola Mundo"; 
	cout<<e<<endl;
	
	return 0;
}
